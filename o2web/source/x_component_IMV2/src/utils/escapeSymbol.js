/**
 * 编码后的字符串还原
 * @param text
 * @returns {string}
 */
export const contentEscapeBackToSymbol = (text) => {
    if (!text || text === '') {
        return ''
    }
    let newText = text + ''
    for (const [key, value] of Object.entries(htmlSymbols())) {
        if (newText.includes(value)) {
            newText = newText.replaceAll(value, key)
        }
    }
    return newText
}